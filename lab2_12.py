import turtle as t
turtle.shape('turtle')
t.fillcolor('yellow')
t.begin_fill()
t.circle(50)
t.end_fill()
t.penup()
t.goto(-20, 60)
t.pendown()
t.fillcolor('blue')
t.begin_fill()
t.circle(10)
t.end_fill()
t.penup()
t.goto(20, 60)
t.pendown()
t.begin_fill()
t.circle(10)
t.end_fill()
t.width(5)
t.penup()
t.goto(0, 55)
t.pendown()
t.goto(0,35)
t.penup()
t.goto(-20,35)
t.rt(90)
t.pendown()
t.color('red')
t.circle(20, 180, 30)
t.exitonclick()
